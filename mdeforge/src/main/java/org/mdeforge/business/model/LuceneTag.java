package org.mdeforge.business.model;

public class LuceneTag {
	
	private String tagName;
	private String artifactKindValue;
	
	public String getTagName() {
		return tagName;
	}
	public void setTagName(String tagName) {
		this.tagName = tagName;
	}
	public String getArtifactKindValue() {
		return artifactKindValue;
	}
	public void setArtifactKindValue(String artifactKindValue) {
		this.artifactKindValue = artifactKindValue;
	}
	
	

}
